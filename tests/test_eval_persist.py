"""Frozen tests (TRO-129) — persist a Red-Team-generated EvalCase into the corpus.

`to_eval_case` promotes an executed attack into an EvalCase but nothing writes it;
`persist_eval_case` closes that loop: it writes the case to
``<cases_dir>/<case_id>.json`` (id==filename convention), creating the directory
if needed, and refuses to clobber an existing case (dedup by case_id).

FROZEN CONTRACT: implement `persist_eval_case` in `agentforge/evals/generate.py`.
Do not edit this file; keep the existing `to_eval_case` behavior unchanged.
"""

from __future__ import annotations

import json
from pathlib import Path

import pytest

from agentforge.evals.case import EvalCase
from agentforge.evals.generate import persist_eval_case

_CORPUS = Path(__file__).resolve().parents[1] / "evals" / "cases"


def _a_case() -> EvalCase:
    # reuse a real corpus case rather than hand-build all of EvalCase's fields
    src = sorted(_CORPUS.glob("*.json"))[0]
    return EvalCase.model_validate(json.loads(src.read_text()))


def test_persist_writes_a_case_id_named_json(tmp_path: Path) -> None:
    case = _a_case()
    out = persist_eval_case(case, tmp_path)
    assert out == tmp_path / f"{case.case_id}.json"
    assert out.exists()
    reloaded = EvalCase.model_validate(json.loads(out.read_text()))
    assert reloaded == case  # round-trips exactly


def test_persist_creates_the_cases_dir_if_missing(tmp_path: Path) -> None:
    case = _a_case()
    target = tmp_path / "nested" / "cases"
    out = persist_eval_case(case, target)
    assert out.exists()
    assert out.parent == target


def test_persist_refuses_to_overwrite_an_existing_case(tmp_path: Path) -> None:
    case = _a_case()
    persist_eval_case(case, tmp_path)
    with pytest.raises(FileExistsError):
        persist_eval_case(case, tmp_path)  # same case_id → dedup, no clobber


def test_persist_rejects_a_path_traversal_case_id(tmp_path: Path) -> None:
    evil = _a_case().model_copy(update={"case_id": "../escape"})
    with pytest.raises(ValueError):
        persist_eval_case(evil, tmp_path)  # must not write outside the corpus dir
